
for n in range(0, 101):
    num = n % 10
    if n == 0:
        print(n, 'процентов')
    elif num == 0:
        print(n, 'процентов')
    elif n >= 11 and n <= 14:
        print(n, 'процентов')
    elif num == 1:
        print(n, 'процент')
    elif num >= 5:
        print(n, 'процентов')
    else:
        print(n, 'процента')



